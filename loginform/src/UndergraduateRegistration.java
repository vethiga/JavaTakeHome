    private void indexKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_indexKeyReleased
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from undergraduateregistration where IndexNo=?";
            PreparedStatement st=connect.prepareStatement(query);
            st.setString(1,index.getText());
            ResultSet rst=st.executeQuery();
            if(rst.next()){
                upindex.setText(rst.getString("IndexNo"));
                upname.setText(rst.getString("FullName"));
                updob.setText(rst.getString("DOB"));
                if(rst.getString("Gender").equals("Male")){
                    upmale.setSelected(true);
                }
                else{
                    upfemale.setSelected(true);
                }
                upaddress.setText(rst.getString("Address"));
                upnic.setText(rst.getString("NICNo"));
                upcont.setText(rst.getString("ContNo"));
                upresult.setText(rst.getString("ALresult"));
                uprank.setText(rst.getString("Rank"));
                String Faculty=rst.getString("Faculty");
                    switch(Faculty){
                        case"School of Engineering":
                            upfaculty.setSelectedIndex(0);
                            break;
                        case"School of Computing":
                            upfaculty.setSelectedIndex(1);
                            break;
                        case"School of Business":
                            upfaculty.setSelectedIndex(2);
                            break;
                    }
                String Course=rst.getString("Course");
                switch(Course){
                    case"Civil Engineering":
                        upcourse.setSelectedIndex(0);
                        break;
                    case"Electrical Engineering":
                        upcourse.setSelectedIndex(1);
                        break;
                    case"Computer Science":
                        upcourse.setSelectedIndex(2);
                        break;
                    case"Business Management":
                        upcourse.setSelectedIndex(3);
                        break;
                }
                
            }
            
               /* else{
                    JOptionPane.showMessageDialog(null,"Entered index number is incorrect");
                }*/
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }           // TODO add your handling code here:
    }//GEN-LAST:event_indexKeyReleased

