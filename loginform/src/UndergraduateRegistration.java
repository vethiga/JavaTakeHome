    public ArrayList<details> userList(){
        ArrayList<details>userList=new ArrayList<>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from undergraduateregistration";
            Statement st=connect.createStatement();
            ResultSet rst=st.executeQuery(query);
            details Details;
            while(rst.next()){
                Details=new details(rst.getString("IndexNo"),rst.getString("FullName"),rst.getString("DOB"),rst.getString("Gender"),rst.getString("Address"),rst.getString("NICNo"),rst.getInt("ContNo"),rst.getString("ALresult"),rst.getInt("Rank"),rst.getString("Faculty"),rst.getString("Course"));
                userList.add(Details);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        return userList;
        
    }
