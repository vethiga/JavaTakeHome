    private void uplsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_uplsearchKeyReleased
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from lecturerdetail where IDNo=?";
            PreparedStatement st=connect.prepareStatement(query);
            st.setString(1,uplsearch.getText());
            ResultSet rst=st.executeQuery();
            if(rst.next()){
                uplid.setText(rst.getString("IDNo"));
                uplname.setText(rst.getString("FullName"));
                upldob.setText(rst.getString("DOB"));
                if(rst.getString("Gender").equals("Male")){
                    uplmale.setSelected(true);
                }
                else{
                    uplfemale.setSelected(true);
                }
                upladdress.setText(rst.getString("Address"));
                uplcont.setText(rst.getString("ContNo"));
                uplsub.setText(rst.getString("Subject"));
                uplsubid.setText(rst.getString("SubjectId"));
                String Faculty=rst.getString("Faculty");
                    switch(Faculty){
                        case"School of Engineering":
                            uplfaculty.setSelectedIndex(0);
                            break;
                        case"School of Computing":
                            uplfaculty.setSelectedIndex(1);
                            break;
                        case"School of Business":
                            uplfaculty.setSelectedIndex(2);
                            break;
                    }
                uplemail.setText(rst.getString("Email"));
                uplhall.setText(rst.getString("LectureHall"));    
            }
            
               /* else{
                    JOptionPane.showMessageDialog(null,"Entered index number is incorrect");
                }*/
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }           // TODO add your handling code here:
    }//GEN-LAST:event_uplsearchKeyReleased

