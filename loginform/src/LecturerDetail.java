     public ArrayList<lecdetails> LecList(){
        ArrayList<lecdetails>LecList=new ArrayList<>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from lecturerdetail";
            Statement st=connect.createStatement();
            ResultSet rst=st.executeQuery(query);
            lecdetails Lecdetails;
            while(rst.next()){
                Lecdetails=new lecdetails(rst.getString("IDNo"),rst.getString("FullName"),rst.getString("DOB"),rst.getString("Gender"),rst.getString("Address"),rst.getInt("ContNo"),rst.getString("subject"),rst.getString("SubjectId"),rst.getString("Faculty"),rst.getString("Email"),rst.getString("LectureHall"));
                LecList.add(Lecdetails);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        return LecList;
        
    }
