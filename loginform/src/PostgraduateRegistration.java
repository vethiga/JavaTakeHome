    public ArrayList<pregdetails>pregList(){
        ArrayList<pregdetails>pregList=new ArrayList<>();
         try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from postgraduateregistration";
            Statement st=connect.createStatement();
            ResultSet rst=st.executeQuery(query);
            pregdetails Pregdetails;
            while(rst.next()){
                Pregdetails=new pregdetails(rst.getString("IndexNo"), rst.getString("FullName"), rst.getString("DOB"), rst.getString("Gender"), rst.getString("Address"), rst.getString("NICNo"), rst.getInt("ContNo"), rst.getString("Qualification"), rst.getString("Institute"), rst.getString("YrCompletion"), rst.getString("Faculty"), rst.getString("Course"));
                pregList.add(Pregdetails);           
            }
         
         } 
         catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
         }
         return pregList;
    }
