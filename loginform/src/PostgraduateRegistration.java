    private void psearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_psearchKeyReleased
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from postgraduateregistration where IndexNo=?";
            PreparedStatement st=connect.prepareStatement(query);
            st.setString(1,psearch.getText());
            ResultSet rst=st.executeQuery();
            if(rst.next()){
                pupindex.setText(rst.getString("IndexNo"));
                pupname.setText(rst.getString("FullName"));
                pupdob.setText(rst.getString("DOB"));
                if(rst.getString("Gender").equals("Male")){
                    pupmale.setSelected(true);
                }
                else{
                    pupfemale.setSelected(true);
                }
                pupaddress.setText(rst.getString("Address"));
                pupnic.setText(rst.getString("NICNo"));
                pupcont.setText(rst.getString("ContNo"));
                pupquali.setText(rst.getString("Qualification"));
                pupinsti.setText(rst.getString("Institute"));
                pupcomple.setText(rst.getString("YrCompletion"));
                String Faculty=rst.getString("Faculty");
                    switch(Faculty){
                        case"School of Engineering":
                            pupfaculty.setSelectedIndex(0);
                            break;
                        case"School of Computing":
                            pupfaculty.setSelectedIndex(1);
                            break;
                        case"School of Business":
                            pupfaculty.setSelectedIndex(2);
                            break;
                    }
                String Course=rst.getString("Course");
                switch(Course){
                    case"Civil Engineering":
                        pupcourse.setSelectedIndex(0);
                        break;
                    case"Electrical Engineering":
                        pupcourse.setSelectedIndex(1);
                        break;
                    case"Computer Science":
                        pupcourse.setSelectedIndex(2);
                        break;
                    case"Business Management":
                        pupcourse.setSelectedIndex(3);
                        break;
                }
                
            }
            
               /* else{
                    JOptionPane.showMessageDialog(null,"Entered index number is incorrect");
                }*/
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }                   // TODO add your handling code here:
    }//GEN-LAST:event_psearchKeyReleased

