    private void inupsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inupsearchKeyReleased
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from instructordetail where IDNo=?";
            PreparedStatement st=connect.prepareStatement(query);
            st.setString(1,inupsearch.getText());
            ResultSet rst=st.executeQuery();
            if(rst.next()){
                inupid.setText(rst.getString("IDNo"));
                inupname.setText(rst.getString("FullName"));
                inupdob.setText(rst.getString("DOB"));
                if(rst.getString("Gender").equals("Male")){
                    inupmale.setSelected(true);
                }
                else{
                    inupfemale.setSelected(true);
                }
                inupaddress.setText(rst.getString("Address"));
                inupcont.setText(rst.getString("ContNo"));
                inupquali.setText(rst.getString("Qualification"));
                inupsub.setText(rst.getString("Subject"));
                inupsubid.setText(rst.getString("SubjectId"));
                String Faculty=rst.getString("Faculty");
                    switch(Faculty){
                        case"School of Engineering":
                            inupfaculty.setSelectedIndex(0);
                            break;
                        case"School of Computing":
                            inupfaculty.setSelectedIndex(1);
                            break;
                        case"School of Business":
                            inupfaculty.setSelectedIndex(2);
                            break;
                    }
                inupemail.setText(rst.getString("Email"));
                inuproom.setText(rst.getString("Room"));    
            }
            
               /* else{
                    JOptionPane.showMessageDialog(null,"Entered index number is incorrect");
                }*/
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }                // TODO add your handling code here:
    }//GEN-LAST:event_inupsearchKeyReleased

