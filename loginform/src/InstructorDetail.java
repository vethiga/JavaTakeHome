    public ArrayList<insdetails> InsList(){
        ArrayList<insdetails>InsList=new ArrayList<>();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect=DriverManager.getConnection("jdbc:mysql://localhost:3306/login?zeroDateTimeBehavior=convertToNull","root","");
            String query="select * from instructordetail";
            Statement st=connect.createStatement();
            ResultSet rst=st.executeQuery(query);
            insdetails Insdetails;
            while(rst.next()){
                Insdetails=new insdetails(rst.getString("IDNo"),rst.getString("FullName"),rst.getString("DOB"),rst.getString("Gender"),rst.getString("Address"),rst.getInt("ContNo"),rst.getString("Qualification"),rst.getString("subject"),rst.getString("SubjectId"),rst.getString("Faculty"),rst.getString("Email"),rst.getString("Room"));
                InsList.add(Insdetails);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        return InsList;
        
    }
